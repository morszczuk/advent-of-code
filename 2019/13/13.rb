class Quiz13A
  def solve
    result = Intcode.new(filename: 'input.txt').run
    output = result[:output]
    print_result(output)
    byebug
  end

  def print_result(output)
    hehe = output.each_slice(3).to_a.group_by{ |aaa| [aaa.first, aaa[1]] }
    max_x = hehe.keys.max {|lala| lala.last }.last
    max_y = hehe.keys.max {|lala| lala.first }.first
    picture = max_x.times.map do |x|
      max_y.times.map do |y|
        # byebug
        map_elem(hehe[[y, x]].last.last)
      end
    end
    result = picture.flatten.count('=')
    picture.each do |row|
      pp row.join('')
    end
    pp result
    # pp "#{picture}"
    byebug
  end

  def map_elem(code)
    # byebug
    case code
    when 0
      ' '
    when 1
      '#'
    when 2
      '='
    when 3
      '-'
    when 4
      'o'
    end
  end
end

